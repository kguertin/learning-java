package com.company;

public class Main {

    public static void main(String[] args) {
        char letter = 'E';

        switch (letter) {
            case 'A':
                System.out.println("Found Letter A");
                break;
            case 'B':
                System.out.println("Found Letter B");
                break;
            case 'C':
                System.out.println("Found Letter C");
                break;
            case 'D':
                System.out.println("Found Letter D");
                break;
            case 'E':
                System.out.println("Found Letter E");
                break;
            default:
                System.out.println("Letter Not Found");
        }

    }
}
